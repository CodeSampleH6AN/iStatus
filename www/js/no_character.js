$('html').bind('keypress', function (e) {
    if (e.keyCode == 32) {
        return false;
    }
    if (e.keyCode == 109) {
        return false;
    }
    if (e.keyCode == 188) {
        return false;
    }
    if (e.keyCode == 189) {
        return false;
    }
    if (e.keyCode == 190) {
        return false;
    }
    if (e.KEYCODE_MINUS = 69) {
        return false;
    }
});
